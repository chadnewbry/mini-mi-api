func TestUploadGenerateCandidatesAndDownloadAsset(t *testing.T) {
	t.Parallel()

	server := newTestServer(t)
	createRecorder := performRequest(t, server.Handler(), http.MethodPost, "/v1/minime/sessions", `{}`)
	var created remoteSessionSnapshot
	decodeJSON(t, createRecorder, &created)

	body := &bytes.Buffer{}
	writer := multipart.NewWriter(body)
	part, err := writer.CreateFormFile("photos", "portrait.png")
	if err != nil {
		t.Fatalf("create multipart file: %v", err)
	}
	if _, err := part.Write(minimalPNG); err != nil {
		t.Fatalf("write multipart file: %v", err)
	}
	if err := writer.Close(); err != nil {
		t.Fatalf("close multipart writer: %v", err)
	}

	uploadRequest := httptest.NewRequest(http.MethodPost, "/v1/minime/sessions/"+created.SessionID+"/photos", body)
	uploadRequest.Header.Set("Authorization", "Bearer test-token")
	uploadRequest.Header.Set("Content-Type", writer.FormDataContentType())
	uploadRecorder := httptest.NewRecorder()
	server.Handler().ServeHTTP(uploadRecorder, uploadRequest)

	if uploadRecorder.Code != http.StatusOK {
		t.Fatalf("expected status %d, got %d: %s", http.StatusOK, uploadRecorder.Code, uploadRecorder.Body.String())
	}

	var uploaded remoteSessionSnapshot
	decodeJSON(t, uploadRecorder, &uploaded)
	if len(uploaded.SourcePhotos) != 1 {
		t.Fatalf("expected 1 source photo, got %d", len(uploaded.SourcePhotos))
	}

	candidateRecorder := performRequest(t, server.Handler(), http.MethodPost, "/v1/minime/sessions/"+created.SessionID+"/candidates:generate", `{}`)
	if candidateRecorder.Code != http.StatusOK {
		t.Fatalf("expected status %d, got %d: %s", http.StatusOK, candidateRecorder.Code, candidateRecorder.Body.String())
	}

	candidateJobID := candidateRecorder.Header().Get("X-MiniMe-Job-ID")
	if candidateJobID == "" {
		t.Fatal("expected X-MiniMe-Job-ID header for candidate generation")
	}
	job := waitForJobTerminal(t, server, candidateJobID)
	if job.Status != "completed" {
		t.Fatalf("expected completed candidate job, got %q", job.Status)
	}

	var candidates remoteSessionSnapshot
	candidates = fetchSessionSnapshot(t, server, created.SessionID)
	if len(candidates.Candidates) == 0 {
		t.Fatal("expected generated candidates")
	}
	if candidates.Candidates[0].DownloadURL == "" {
		t.Fatal("expected candidate download URL")
	}

	downloadPath := strings.TrimPrefix(candidates.Candidates[0].DownloadURL, "http://example.com")
	downloadRecorder := performRequest(t, server.Handler(), http.MethodGet, downloadPath, "")
	if downloadRecorder.Code != http.StatusOK {
		t.Fatalf("expected status %d, got %d: %s", http.StatusOK, downloadRecorder.Code, downloadRecorder.Body.String())
	}
	if contentType := downloadRecorder.Header().Get("Content-Type"); contentType == "" {
		t.Fatal("expected content-type for downloaded asset")
	}
	if !bytes.Equal(downloadRecorder.Body.Bytes(), minimalPNG) {
		t.Fatal("expected downloaded asset bytes to match uploaded placeholder content")
	}
}

