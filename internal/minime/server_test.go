func waitForJobTerminal(t *testing.T, server *Server, jobID string) remoteJobSnapshot {
	t.Helper()

	deadline := time.Now().Add(5 * time.Second)
	for time.Now().Before(deadline) {
		jobRecorder := performRequest(t, server.Handler(), http.MethodGet, "/v1/minime/jobs/"+jobID, "")
		if jobRecorder.Code != http.StatusOK {
			t.Fatalf("expected status %d, got %d: %s", http.StatusOK, jobRecorder.Code, jobRecorder.Body.String())
		}

		var job remoteJobSnapshot
		decodeJSON(t, jobRecorder, &job)
		if job.Status == "completed" || job.Status == "failed" {
			return job
		}

		time.Sleep(20 * time.Millisecond)
	}

	t.Fatalf("timed out waiting for job %s to finish", jobID)
	return remoteJobSnapshot{}
}

